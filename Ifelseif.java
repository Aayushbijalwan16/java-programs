public class Ifelseif {
        public static void main(String arg[]){
            int x = 57;
            int y = 41;
            int z = 95;
    
            if (x>y && x>z) {

                System.out.println(x); 
            }
            else if (y>z) {
                System.out.println(y);
        }
        else {
                System.out.println(z);
            }
    }
}
